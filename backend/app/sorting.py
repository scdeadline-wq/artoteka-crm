"""Сортировка по фамилии.

Данные художников хранятся в формате «Имя Фамилия» (напр. «Александр Быховский»,
«Алексей Смирнов»), поэтому фамилия = ПОСЛЕДНЕЕ слово в name_ru. Дополнительно
убираем скобочные пояснения, чтобы «Алексей Смирнов (фон Раух)» → «Смирнов».

Фолбэк (одно слово, инициалы и т.п.) — берётся вся оставшаяся строка.
Edge-кейсы: записи в обратном формате «Фамилия Имя» (напр. «Куранская Ольга»)
отсортируются по второму слову, группы через запятую — по последней фамилии.
"""
from sqlalchemy import func
from sqlalchemy.sql.elements import ColumnElement


def surname_expr(col: ColumnElement) -> ColumnElement:
    """SQL-выражение «фамилия» для ORDER BY (Postgres). Регистронезависимо."""
    # 1) убрать «(...)» вместе с пробелом перед ними
    no_parens = func.regexp_replace(col, r"\s*\([^)]*\)", "", "g")
    # 2) обрезать пробелы по краям (в данных встречается хвостовой пробел)
    trimmed = func.btrim(no_parens)
    # 3) оставить только последнее слово (всё до последнего пробела — выкинуть)
    last_word = func.regexp_replace(trimmed, r"^.*\s", "")
    return func.lower(last_word)
